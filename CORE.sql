-- SET CONTEXT
USE WAREHOUSE FACTORY_ANALYTICS_WH;
USE DATABASE FACTORY_ANALYTICS_DB;
USE SCHEMA CORE;


-- CORE FACTORY TABLE
CREATE OR REPLACE TABLE CORE_FACTORY (
    FACTORY_ID STRING PRIMARY KEY,
    FACTORY_NAME STRING,
    CITY STRING,
    OPERATIONAL_STATUS STRING,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP()
);


-- CORE MACHINE TABLE
CREATE OR REPLACE TABLE CORE_MACHINE (
    MACHINE_ID STRING PRIMARY KEY,
    MACHINE_NAME STRING,
    MACHINE_TYPE STRING,
    INSTALLATION_DATE DATE,
    FACTORY_ID STRING,
    MACHINE_STATUS STRING DEFAULT 'ACTIVE',
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP()
);


-- CORE PRODUCTION BATCH TABLE
CREATE OR REPLACE TABLE CORE_PRODUCTION_BATCH (
    BATCH_ID STRING PRIMARY KEY,
    MACHINE_ID STRING,
    PRODUCTION_DATE DATE,
    QUANTITY_PRODUCED NUMBER,
    DEFECT_COUNT NUMBER,
    GOOD_UNITS NUMBER,
    DEFECT_PERCENT NUMBER(10,2),
    EFFICIENCY_PERCENT NUMBER(10,2),
    BATCH_STATUS STRING,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP()
);


-- CORE MAINTENANCE LOG TABLE
CREATE OR REPLACE TABLE CORE_MAINTENANCE_LOG (
    LOG_ID STRING PRIMARY KEY,
    MACHINE_ID STRING,
    MAINTENANCE_DATE DATE,
    ISSUE_TYPE STRING,
    RESOLVED_STATUS STRING,
    MACHINE_CONDITION STRING,
    DOWNTIME_STATUS STRING,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP()
);


-- CORE QUALITY ALERT TABLE
CREATE OR REPLACE TABLE CORE_QUALITY_ALERT (
    ALERT_ID STRING,
    BATCH_ID STRING,
    QUALITY_SCORE NUMBER,
    ALERT_REASON STRING,
    ALERT_STATUS STRING,
    ALERT_PRIORITY STRING,
    ALERT_CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP()
);


